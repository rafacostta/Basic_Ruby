# CLASSE FIXNUM
    # Basicamente são valores inteiros, seu intervalo de valor depende do sistema operacional.

# CLASSE BIGNUM
    # São números inteiros que estão fora do intervalo da classe Fixnum.

# CLASSE FLOAT
    # São npumeros com ponto flutuante, números reais.

# CLASSE RATIONAL
    # Número que pode ser expresso por uma fração.


